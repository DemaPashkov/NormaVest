       <p>
       Оценить бизнес - значит определить рыночную стоимость всего капитала предприятия. Это бывает необходимо, для вступления в наследство или если под залог акций фирмы выдается кредит коммерческими банками.Также, оценка бизнеса необходима для определения факторов влияния инвестиционной деятельности на рыночную стоимость организации. Обязательно оценивают предприятие в тех случаях, если его собираются продавать, или для страхования, чтобы указать сумму, на которую предприятие нужно застраховать. <br>
       <br>
Выделяют четыре оценочных подхода:<br>
<br>
1. Затратный - определяется по методике чистых активов, т. е. на основе отраженной в бухгалтерских счетах разницы стоимости активов и пассивов.
<br><br>
2. Доходный - определяется по денежным выгодам собственников предприятия. Здесь главную роль играет очевидный принцип того, что предполагаемый покупатель не назначит цену за предприятие выше, чем оно сможет принести ему выгоду.
<br><br>
3. Сравнительный - определяется на основе реальных цен аналогичных предприятий.
<br><br>
Все подходы используются в совокупности, - различается лишь доля влияния подхода на конечную величину стоимости предприятия.
<br><br>
4. Дополнительный эмпирический подход, при котором годовой объем дохода равен общему денежному потоку, в который включают также чистую прибыль фирмы и заработную плату владельцев бизнеса.
<br><br>
Полученные в итоге данные часто могут значительно различаться, поэтому все методы применяются совокупно, и доля каждого из них в результате участвует в получении единой стоимости объекта.
<br><br>
Рассчитывается эта стоимость как средневзвешенная величина. На основе оценки бизнеса представляется оценочный отчет, включающий в себя данные о стоимости основных материальных и нематериальных активов предприятия, а также об эффективности работы предприятия. Анализируются все доходы, а также возможность их получения в дальнейшем; оценивается конкурентоспособность предприятия.
<br><br>
Оценка акций, также как и оценка векселей, производится с целью определить вероятную, но максимально точную цену, по которой их продадут на рынке ценных бумаг, чтобы принести прибыль их владельцу. Получить выгоду от продажи акции, векселя или другой ценной бумаги можно путем получения процентов, либо приобретения векселя меньшую сумму, чем его номинал.
<br><br>
Оценка ценных бумаг характеризует степень надежности продавца, плательщика или векселедателя. Чаще всего оценивают именно рыночную стоимость ценных бумаг,так как ее почти всегда используют при различных платежных операциях. Стоимость ценных бумаг в итоге зависит от их доходности и надежности предприятия.
<br><br>
В некоторых случаях коммерческая сделка может сорваться по вине контрагента и предприятие может потребовать возмещения упущенной выгоды (неполученных доходов). Доказывает сам факт нарушения сделки всегда истец.
<br><br>
Оценка упущенной выгоды производится довольно легко: она равна сумме, которую могло бы получить предприятие, но не получило, так как его право на это было нарушено не по его вине.
<br><br>
При этом нужно определить, не было ли объективного фактора нарушения условий сделки, например, колебания цен и объема продукции в зависимости от сезона или срыв сделки из-за чрезвычайных происшествий. При этом сделка может быть произведена не полностью, например, продукцию недопоставили. В этом случае разность между стоимостью контракта и реально выполненными услугами и будет составлять неполученный доход.
<br><br>
В последнее время предприятия переходят на Международные стандарты финансовой отчетности (МСФО). Это необходимо для выхода предприятия на международные рынки и, следовательно, привлечения новых иностранных инвесторов.
<br><br>
Оценка для целей МСФО происходит по международным унифицированным стандартам, которые важно знать специалистам оценочных компаний. При оценке учитывают стоимость активов и финансовую отчетность предприятия.
<br><br>
Оценка бизнеса для вступления в наследство– обязательная процедура для оформления права наследования. Наследуемое предприятие или фирма часто имеют в своем владении имущество, рыночную стоимость которого необходимо учитывать при оценке бизнеса и её нотариальном оформлении.
<br><br>
Земля и недвижимость предприятия часто передаются по наследству без особых правил, - как обычное имущество. Сложности могут возникнуть, например, при наследовании товарных знаков и патентов. Также часто возникают случаи, когда устав предприятия требует согласия на переход доли по наследству других участников организации. Именно поэтому, необходимо обращаться к специалистам, чтобы установить, какой долей предприятия владеете именно вы, чтобы передать её по наследству. 
